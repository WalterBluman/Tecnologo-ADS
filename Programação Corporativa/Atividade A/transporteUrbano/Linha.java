package transporteUrbano;

public class Linha {
	private String numero;
	private String nome;
	private Rota percurso;
	
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Rota getPercurso() {
		return percurso;
	}
	public void setPercurso(Rota percurso) {
		this.percurso = percurso;
	}
	
	
	
	
}
