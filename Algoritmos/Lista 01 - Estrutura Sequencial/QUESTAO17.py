"""17. Leia o valor da base e altura de um retângulo, calcule e escreva sua área. (área = base * altura) """
b = float(input('Digite o valor da base do retângulo: '))
h = float(input('Digite o valor da altura do retângulo: '))
a = b*h
print ('O retângulo de base %.1f e altura %.1f tem área %.1f' % (b, h, a))
